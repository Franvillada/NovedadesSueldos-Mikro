    public function login(Request $request)
    {
        $users = User::all();
        foreach ($users as $user) {
            if($user->usuario == $request->usuario){
                if($user->contraseña == $request->contraseña){
                    return redirect()->route('userHome',$user->usuario);
                }
            }
        }
        return redirect()->route('home')->with('error','El usuario o la contraseña ingresadas no son correctas');
    }
    
